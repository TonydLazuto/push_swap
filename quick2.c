#include "push_swap.h"

void	set_num_pos(t_num **stack)
{
	t_num	*tmp;
	int		i;

	tmp = *stack;
	i = 0;
	if (*stack)
	{
		while (tmp)
		{
			tmp->pos = i;
			tmp = tmp->next;
			i++;
		}
	}
}
