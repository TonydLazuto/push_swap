#include "push_swap.h"

void	swap_two(int *nb)
{
	if (nb[0] > nb[1])
		ft_putendl_fd("sa", 1);
	if (nb[0] == nb[1])
		ft_error(nb);
}

void	swap_three(int *nb)
{
	if (nb[0] == nb[1] || nb[1] == nb[2] || nb[2] == nb[0])
		ft_error(nb);
}
